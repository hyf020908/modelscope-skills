def detect_local_runtime() -> dict[str, Any]:
    system = platform.system().lower()
    return {
        "system": system,
        "is_linux": system == "linux",
        "swift_on_path": shutil.which("swift") is not None,
        "nvidia_smi_on_path": shutil.which("nvidia-smi") is not None,
    }


def build_required_env(root: Path, runtime: dict[str, Any]) -> dict[str, str]:
    existing = parse_env_file(root / "configs" / "local.required.env")
    return {
        "LOCAL_HOST_OS": existing.get("LOCAL_HOST_OS", "linux" if runtime["is_linux"] else runtime["system"] or "linux"),
        "LOCAL_WORKDIR": existing.get("LOCAL_WORKDIR", str(root)),
        "SWIFT_BIN": existing.get("SWIFT_BIN", "swift"),
        "PYTHON_BIN": existing.get("PYTHON_BIN", "python"),
        "CUDA_VISIBLE_DEVICES": existing.get("CUDA_VISIBLE_DEVICES", os.environ.get("CUDA_VISIBLE_DEVICES", "0")),
        "NPROC_PER_NODE": existing.get("NPROC_PER_NODE", os.environ.get("NPROC_PER_NODE", "1")),
        "TRAIN_LOG_DIR": existing.get("TRAIN_LOG_DIR", str(root / "outputs" / "logs")),
        "TRAIN_HOST_NOTE": existing.get("TRAIN_HOST_NOTE", "review_then_continue"),
    }


def write_jsonl_if_missing(path: Path, rows: list[dict[str, Any]], force: bool) -> bool:
    if path.exists() and not force:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        "\n".join(json.dumps(row, ensure_ascii=False) for row in rows) + "\n",
        encoding="utf-8",
    )
    return True


def normalize_method(raw: str) -> str:
    text = raw.lower()
    if any(token in text for token in ["grpo", "rlhf", "强化学习", "reinforcement"]):
        return "grpo"
    if any(token in text for token in ["dpo", "偏好优化", "preference optimization"]):
        return "dpo"
    return "sft"


def detect_train_type(request: str) -> str:
    lowered = request.lower()
    if "qlora" in lowered:
        return "qlora"
    if "全参数" in request or "full parameter" in lowered or re.search(r"\bfull\b", lowered):
        return "full"
    return "lora"


def is_probable_repo_id(token: str) -> bool:
    lowered = token.lower()
    if token.startswith((".", "/", "~")):
        return False
    bad_suffixes = (
        ".json",
        ".jsonl",
        ".csv",
        ".tsv",
        ".parquet",
        ".txt",
        ".jpg",
        ".jpeg",
        ".png",
        ".webp",
    )
    return not lowered.endswith(bad_suffixes)


def extract_repo_like(request: str, keywords: list[str]) -> str:
    lowered = request.lower()
    repo_pat = re.compile(r"(?<![/.\w-])([A-Za-z0-9_.-]+/[A-Za-z0-9_.-]+(?:#[A-Za-z0-9_.-]+)?)")
    for keyword in keywords:
        idx = lowered.find(keyword.lower())
        if idx == -1:
            continue
        snippet = request[idx : idx + 160]
        match = repo_pat.search(snippet)
        if match and is_probable_repo_id(match.group(1)):
            return match.group(1)
    return ""


def extract_path_like(request: str, keywords: list[str]) -> str:
    lowered = request.lower()
    path_pat = re.compile(
        r"(?:^|[\s'\"=：:])(?P<path>\./[^\s,，;；]+|\.\./[^\s,，;；]+|/[^\s,，;；]+|~/[^\s,，;；]+)"
    )
    for keyword in keywords:
        idx = lowered.find(keyword.lower())
        if idx == -1:
            continue
        snippet = request[idx : idx + 160]
        match = path_pat.search(snippet)
        if match:
            return match.group("path")
    return ""


def extract_first_local_path(request: str) -> str:
    path_pat = re.compile(
        r"(?:^|[\s'\"=：:])(?P<path>\./[^\s,，;；]+|\.\./[^\s,，;；]+|/[^\s,，;；]+|~/[^\s,，;；]+)"
    )
    match = path_pat.search(request)
    return match.group("path") if match else ""


def extract_scalar_params(request: str) -> dict[str, str]:
    params: dict[str, str] = {}
    pattern = re.compile(
        r"(?P<key>learning rate|learning_rate|lr|学习率|batch size|batch_size|训练批大小|批大小|"
        r"grad acc|grad_acc|gradient accumulation|gradient_accumulation_steps|梯度累积|"
        r"epochs|epoch|轮数|max steps|max_steps|save steps|save_steps|eval steps|eval_steps|"
        r"logging steps|logging_steps|max length|max_length|上下文长度|序列长度|lora rank|lora_rank|"
        r"beta|loss type|loss_type|train type|train_type|微调方式|use vllm|use_vllm)"
        r"\s*[:=：]\s*(?P<value>[^\s,，;；]+)",
        re.IGNORECASE,
    )
    for match in pattern.finditer(request):
        key = KEY_ALIASES[match.group("key").lower()]
        params[key] = match.group("value")
    return params


def build_messages(user_text: str, assistant_text: str) -> list[dict[str, str]]:
    return [
        {"role": "system", "content": DEFAULT_SYSTEM},
        {"role": "user", "content": user_text},
        {"role": "assistant", "content": assistant_text},
    ]


