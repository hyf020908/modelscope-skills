def ensure_default_dataset(root: Path, method: str, force: bool) -> dict[str, Any]:
    train_rows, valid_rows = default_rows(method)
    data_dir = root / "data" / "local" / method
    train_path = data_dir / "train.jsonl"
    valid_path = data_dir / "valid.jsonl"
    changed = []
    if write_jsonl_if_missing(train_path, train_rows, force):
        changed.append(str(train_path))
    if write_jsonl_if_missing(valid_path, valid_rows, force):
        changed.append(str(valid_path))
    return {
        "train_path": train_path,
        "valid_path": valid_path,
        "changed_files": changed,
    }


def build_request(args: argparse.Namespace) -> dict[str, Any]:
    request = (args.request or "").strip()
    method = args.method or normalize_method(request)
    model_id = args.model or extract_repo_like(request, ["模型", "model", "base model", "基础模型"])
    if not model_id:
        model_id = DEFAULT_MODEL

    dataset = args.dataset or extract_repo_like(request, ["数据集", "dataset"])
    if not dataset:
        dataset = extract_path_like(request, ["数据集", "dataset", "训练集"])
    if not dataset:
        dataset = extract_first_local_path(request)

    params = METHOD_DEFAULTS[method].copy()
    params.update(extract_scalar_params(request))
    if "train_type" not in params or not params["train_type"]:
        params["train_type"] = detect_train_type(request)
    elif params["train_type"].lower() not in {"lora", "qlora", "full"}:
        params["train_type"] = detect_train_type(request)

    output_dir = args.output_dir or f"./outputs/local-{method}"
    return {
        "request_text": request,
        "method": method,
        "model_id": model_id,
        "dataset": dataset,
        "output_dir": output_dir,
        "system_prompt": DEFAULT_SYSTEM,
        "params": params,
    }


def build_local_env(request_cfg: dict[str, Any], train_dataset: str, valid_dataset: str | None) -> dict[str, str]:
    params = request_cfg["params"]
    env = {
        "MODEL_ID": request_cfg["model_id"],
        "TRAINER_METHOD": request_cfg["method"],
        "TRAIN_DATASET": train_dataset,
        "OUTPUT_DIR": request_cfg["output_dir"],
        "TRAIN_TYPE": params["train_type"],
        "MAX_LENGTH": params["max_length"],
        "BATCH_SIZE": params["batch_size"],
        "GRAD_ACC": params["grad_acc"],
        "LEARNING_RATE": params["learning_rate"],
        "EPOCHS": params["epochs"],
        "MAX_STEPS": params["max_steps"],
        "SAVE_STEPS": params["save_steps"],
        "EVAL_STEPS": params["eval_steps"],
        "LOGGING_STEPS": params["logging_steps"],
        "LORA_RANK": params["lora_rank"],
        "SYSTEM_PROMPT": request_cfg["system_prompt"],
        "EXTRA_SWIFT_ARGS": params["extra_swift_args"],
        "LOCAL_TRAIN_NOT_EXECUTED": "true",
    }
    if valid_dataset:
        env["VALID_DATASET"] = valid_dataset
    if request_cfg["method"] == "dpo":
        env["BETA"] = params["beta"]
        env["LOSS_TYPE"] = params["loss_type"]
    if request_cfg["method"] == "grpo":
        env["USE_VLLM"] = params["use_vllm"]
    return env


def build_command_preview(env: dict[str, str]) -> str:
    method = env["TRAINER_METHOD"].lower()
    if method == "sft":
        cmd = [
            "swift",
            "sft",
            "--model",
            env["MODEL_ID"],
            "--dataset",
            env["TRAIN_DATASET"],
            "--output_dir",
            env["OUTPUT_DIR"],
        ]
    else:
        cmd = [
            "swift",
            "rlhf",
            "--rlhf_type",
            method,
            "--model",
            env["MODEL_ID"],
            "--dataset",
            env["TRAIN_DATASET"],
            "--output_dir",
            env["OUTPUT_DIR"],
        ]
    cmd.extend(
        [
            "--train_type",
            env["TRAIN_TYPE"],
            "--max_length",
            env["MAX_LENGTH"],
            "--per_device_train_batch_size",
            env["BATCH_SIZE"],
            "--gradient_accumulation_steps",
            env["GRAD_ACC"],
            "--learning_rate",
            env["LEARNING_RATE"],
            "--num_train_epochs",
            env["EPOCHS"],
            "--max_steps",
            env["MAX_STEPS"],
            "--save_steps",
            env["SAVE_STEPS"],
            "--eval_steps",
            env["EVAL_STEPS"],
            "--logging_steps",
            env["LOGGING_STEPS"],
            "--system",
            env["SYSTEM_PROMPT"],
        ]
    )
    if env["TRAIN_TYPE"] in {"lora", "qlora"}:
        cmd.extend(["--lora_rank", env["LORA_RANK"]])
    if env.get("VALID_DATASET"):
        cmd.extend(["--val_dataset", env["VALID_DATASET"]])
    if method == "dpo":
        cmd.extend(["--beta", env["BETA"], "--loss_type", env["LOSS_TYPE"]])
    if method == "grpo":
        cmd.extend(["--use_vllm", env["USE_VLLM"]])
    extra = env.get("EXTRA_SWIFT_ARGS", "").strip()
    rendered = " ".join(cmd)
    if extra:
        rendered = f"{rendered} {extra}"
    return rendered


def main() -> None:
    parser = argparse.ArgumentParser(description="Prepare local training configs and a launch script")
    parser.add_argument("--request", default="", help="Natural-language training request")
    parser.add_argument("--root", default=".", help="Workspace root")
    parser.add_argument("--method", choices=["sft", "dpo", "grpo"], help="Override method")
    parser.add_argument("--model", default="", help="Override model id")
    parser.add_argument("--dataset", default="", help="Override dataset id or local path")
    parser.add_argument("--output-dir", default="", help="Override output directory")
    parser.add_argument("--force", action="store_true", help="Overwrite generated local example dataset")
    args = parser.parse_args()

    root = Path(args.root).resolve()
    request_cfg = build_request(args)
    dataset_bootstrap = None
    changed_files: list[str] = []

    if request_cfg["dataset"]:
        train_dataset = request_cfg["dataset"]
        valid_dataset = ""
    else:
        dataset_bootstrap = ensure_default_dataset(root, request_cfg["method"], force=args.force)
        changed_files.extend(dataset_bootstrap["changed_files"])
        train_dataset = dataset_bootstrap["train_path"].as_posix()
        valid_dataset = dataset_bootstrap["valid_path"].as_posix()

    env = build_local_env(request_cfg, train_dataset, valid_dataset or None)
    configs_dir = root / "configs"
    scripts_dir = root / "scripts"
    write_env_file(configs_dir / "local.train.env", env)

    command = build_command_preview(env)
    launch_path = scripts_dir / "run_local_training.sh"
    required_env = build_required_env(root, detect_local_runtime())
    write_env_file(configs_dir / "local.required.env", required_env)
    write_shell_script(launch_path, root)
    runtime = detect_local_runtime()

    plan = {
        "request": request_cfg,
        "train_dataset": train_dataset,
        "valid_dataset": valid_dataset,
        "generated_files": {
            "required_env_file": str(configs_dir / "local.required.env"),
            "env_file": str(configs_dir / "local.train.env"),
            "launch_script": str(launch_path),
        },
        "local_dataset_bootstrap": None
        if dataset_bootstrap is None
        else {
            "train_path": str(dataset_bootstrap["train_path"]),
            "valid_path": str(dataset_bootstrap["valid_path"]),
        },
        "run_command_preview": command,
        "launch_command": f"bash {launch_path.relative_to(root).as_posix()}",
        "runtime_check": runtime,
        "wait_for_continue": True,
        "continue_gate_file": str(configs_dir / "local.required.env"),
        "prepared_only": True,
        "not_executed": True,
    }
    write_json(configs_dir / "local.training.plan.json", plan)

    result = {
        "workspace_root": str(root),
        "method": request_cfg["method"],
        "model_id": request_cfg["model_id"],
        "train_dataset": train_dataset,
        "valid_dataset": valid_dataset,
        "generated_files": [
            str(configs_dir / "local.training.plan.json"),
            str(configs_dir / "local.required.env"),
            str(configs_dir / "local.train.env"),
            str(launch_path),
        ],
        "changed_files": changed_files,
        "run_command_preview": command,
        "launch_command": f"bash {launch_path.relative_to(root).as_posix()}",
        "runtime_check": runtime,
        "wait_for_continue": True,
        "continue_gate_file": str(configs_dir / "local.required.env"),
        "prepared_only": True,
        "not_executed": True,
    }
    print(json.dumps(result, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
